library(CoCo)
data(Fuchs82)
enterModel("mdp,da,am,dg,gs,sa", object=Fuchs82)
propertyModel("graphical")
propertyModel("decomposable")
propertyModel("connected")
propertyModel("tree")

enterModel()
backward(recursive = TRUE, follow = TRUE)
makeCurrent("last")
showModel("all")
propertyModel("graphical")
propertyModel("decomposable")
propertyModel("connected")
propertyModel("tree")

editModel("drop.edges", "md")
makeCurrent("last")
showModel("all")
propertyModel("graphical")
propertyModel("decomposable")
propertyModel("connected")
propertyModel("tree")

quitCoCo()
